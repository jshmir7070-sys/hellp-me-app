export const quantityOptions = Array.from({ length: 20 }, (_, i) => (i + 1) * 50);

export const generatePriceOptions = (minPrice: number, maxPrice: number = 10000): number[] => {
  const options: number[] = [];
  const start = Math.ceil(minPrice / 100) * 100;
  for (let price = start; price <= maxPrice; price += 100) {
    options.push(price);
  }
  return options;
};

export const formatPhoneNumber = (value: string): string => {
  const numbers = value.replace(/[^\d]/g, '');
  if (numbers.length <= 3) return numbers;
  if (numbers.length <= 7) return `${numbers.slice(0, 3)}-${numbers.slice(3)}`;
  return `${numbers.slice(0, 3)}-${numbers.slice(3, 7)}-${numbers.slice(7, 11)}`;
};
